import numpy as np

def main():
    # 假设已有矩阵
    matrix = np.array([
        [0.063, 0.089, 0.075, 0.011, 0.013, 0.106, 0.044, 0.515, 0.031, 0.053],
        [0.111, 0.037, 0.092, 0.021, 0.008, 0.064, 0.012, 0.589, 0.044, 0.021],
        [0.186, 0.056, 0.093, 0.007, 0.047, 0.157, 0.079, 0.186, 0.079, 0.111],
        [0.063, 0.074, 0.482, 0.005, 0.016, 0.016, 0.006, 0.063, 0.054, 0.221],
        [0.097, 0.041, 0.204, 0.014, 0.017, 0.025, 0.032, 0.379, 0.180, 0.011],
        [0.088, 0.113, 0.144, 0.006, 0.015, 0.041, 0.025, 0.393, 0.088, 0.088],
        [0.058, 0.018, 0.052, 0.001, 0.002, 0.003, 0.004, 0.846, 0.007, 0.009],
        [0.188, 0.010, 0.032, 0.003, 0.003, 0.188, 0.078, 0.058, 0.252, 0.188],
        [0.045, 0.034, 0.212, 0.002, 0.005, 0.011, 0.015, 0.653, 0.005, 0.019],
        [0.128, 0.128, 0.242, 0.010, 0.012, 0.068, 0.103, 0.128, 0.023, 0.158],
    ])

    # 修改指定位置的值
    matrix[0, 0] = 0.25  # (1,1) 改为 0.25
    matrix[0, 7] = 0.5   # (1,8) 改为 0.5
    matrix[0, 9] = 0.25  # (1,10) 改为 0.25
    matrix[7, 0] = 0.4   # (8,1) 改为 0.4
    matrix[7, 7] = 0.2   # (8,8) 改为 0.2
    matrix[7, 9] = 0.4   # (8,10) 改为 0.4
    matrix[9, 0] = 0.333 # (10,1) 改为 0.333
    matrix[9, 7] = 0.333 # (10,8) 改为 0.333
    matrix[9, 9] = 0.333 # (10,10) 改为 0.333

    # 将其他位置的值改为 0
    # 首先创建一个全为 0 的矩阵
    new_matrix = np.zeros_like(matrix)

    # 仅保留指定位置的值
    indices = [
        (0, 0), (0, 7), (0, 9),  # (1,1), (1,8), (1,10)
        (7, 0), (7, 7), (7, 9),  # (8,1), (8,8), (8,10)
        (9, 0), (9, 7), (9, 9),  # (10,1), (10,8), (10,10)
    ]
    for i, j in indices:
        new_matrix[i, j] = matrix[i, j]

    # 保存修改后的矩阵
    np.save("sparse_matrix.npy", new_matrix)
    print("矩阵已保存为 sparse_matrix.npy 文件")

    # 打印修改后的矩阵
    for row in new_matrix:
        formatted_row = ["{:.3f}".format(x) for x in row]
        print(" ".join(formatted_row))

if __name__ == "__main__":
    main()
